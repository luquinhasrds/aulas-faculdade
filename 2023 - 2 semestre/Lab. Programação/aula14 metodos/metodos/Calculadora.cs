class Calculadora{
    public int Somar(int n1, int n2){
        return n1 + n2;
    }
    public int Somar(int n1, int n2, int n3){
        return n1 + n2 + n3;
    }
    public double Somar(double d1, double d2){
        return d1 + d2;
    }
    public int Subtrair(int n1, int n2){
        return n1 - n2;
    }
    public int Mult(int n1,int n2){
        return n1 + n2;
    }
}